/**
 * Enumeration representing the different types of Funko Pop products.
 *
 * This enumeration defines the various product lines that Funko offers,
 * each with its own unique style and design.
 */
export enum funkoType {
  /** Standard Funko Pop! figures, the most common product line. */
  POP = "Pop!",

  /** Funko Pop! figures featuring characters with their iconic vehicles. */
  POP_RIDES = "Pop! Rides",

  /** Vinyl Soda collectible figures, typically packaged in soda can-like containers. */
  VYNIL_SODA = "Vinyl Soda",

  /** Vinyl Gold figures, a premium Funko line with a stylized design. */
  VYNIL_GOLD = "Vinyl Gold",
}
